  /* Fade-up on scroll */
  const obs = new IntersectionObserver((entries) => {
    entries.forEach(e => {
      if (e.isIntersecting) {
        e.target.classList.add('visible');
        obs.unobserve(e.target);
      }
    });
  }, {
    threshold: 0.1
  });
  document.querySelectorAll('.fade-up').forEach(el => obs.observe(el));
