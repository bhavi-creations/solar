<script>
  /* ── NAVBAR SCROLL ──── */
  const nav = document.getElementById('mainNav');
  window.addEventListener('scroll', () => {
    nav.classList.toggle('scrolled', window.scrollY > 40);
    document.getElementById('backTop').classList.toggle('show', window.scrollY > 400);
  });

  /* ── FADE-UP OBSERVER ── */
  const fadeEls = document.querySelectorAll('.fade-up');
  const observer = new IntersectionObserver((entries) => {
    entries.forEach(e => {
      if (e.isIntersecting) {
        e.target.classList.add('visible');
        observer.unobserve(e.target);
      }
    });
  }, {
    threshold: 0.12
  });
  fadeEls.forEach(el => observer.observe(el));

  /* ── COUNTER ANIMATION ── */
  function animateCounter(el) {
    const target = +el.dataset.target;
    const duration = 1800;
    const step = target / (duration / 16);
    let current = 0;
    const timer = setInterval(() => {
      current += step;
      if (current >= target) {
        el.textContent = target;
        clearInterval(timer);
      } else {
        el.textContent = Math.floor(current);
      }
    }, 16);
  }
  const counterObserver = new IntersectionObserver((entries) => {
    entries.forEach(e => {
      if (e.isIntersecting) {
        e.target.querySelectorAll('.count-num').forEach(animateCounter);
        counterObserver.unobserve(e.target);
      }
    });
  }, {
    threshold: 0.3
  });
  document.querySelectorAll('#hero, #benefits').forEach(s => counterObserver.observe(s));

  /* ── SMOOTH NAV CLOSE ON MOBILE ── */
  document.querySelectorAll('#navMenu .nav-link').forEach(link => {
    link.addEventListener('click', () => {
      const c = bootstrap.Collapse.getInstance(document.getElementById('navMenu'));
      if (c) c.hide();
    });
  });
</script>

<script>
  document.addEventListener('DOMContentLoaded', function() {
